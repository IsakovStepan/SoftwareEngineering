one = [12, 25, 3, 48, 71]
two = [5, 18, 40, 62, 98]
three = [4, 21, 37, 56, 84]

print(f"one = {one}")
print(f"two = {two}")
print(f"three = {three}")

min_one = min(one)
min_two = min(two)
min_three = min(three)

max_one = max(one)
max_two = max(two)
max_three = max(three)

print(f"Минимальные элементы: {min_one}, {min_two}, {min_three}")
print(f"Максимальные элементы: {max_one}, {max_two}, {max_three}")